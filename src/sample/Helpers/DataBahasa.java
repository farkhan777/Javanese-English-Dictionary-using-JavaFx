package sample.Helpers;

public class DataBahasa {
    String jawa,bahasa;

    public DataBahasa(String jawa, String bahasa) {
        this.jawa = jawa;
        this.bahasa = bahasa;
    }

    public String getJawa() {
        return jawa;
    }

    public void setJawa(String jawa) {
        this.jawa = jawa;
    }

    public String getBahasa() {
        return bahasa;
    }

    public void setBahasa(String bahasa) {
        this.bahasa = bahasa;
    }
}
