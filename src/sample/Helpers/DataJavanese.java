package sample.Helpers;

public class DataJavanese {
    String bahasa,jawa;

    public DataJavanese(String bahasa, String jawa) {
        this.bahasa = bahasa;
        this.jawa = jawa;
    }

    public String getBahasa() {
        return bahasa;
    }

    public void setBahasa(String bahasa) {
        this.bahasa = bahasa;
    }

    public String getJawa() {
        return jawa;
    }

    public void setJawa(String jawa) {
        this.jawa = jawa;
    }
}
